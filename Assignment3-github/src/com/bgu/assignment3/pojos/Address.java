package com.bgu.assignment3.pojos;

public class Address {
	
	public Address() {
		
	}
	
	public Address(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	int x;
	int y;
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	

}
